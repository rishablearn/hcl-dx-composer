/**
 * AI Image Generation Routes
 * Handles AI-powered creative generation and integration with DAM workflow
 */

const express = require('express');
const router = express.Router();
const path = require('path');
const db = require('../config/database');
const aiService = require('../services/aiService');
const dxService = require('../services/dxService');
const { authenticateToken, requireAuthor } = require('../middleware/auth');
const logger = require('../config/logger');

/**
 * GET /api/ai/providers
 * Get available AI providers and their capabilities
 */
router.get('/providers', authenticateToken, (req, res) => {
  try {
    const providers = aiService.getAvailableProviders();
    res.json(providers);
  } catch (error) {
    logger.error('Error fetching AI providers:', error);
    res.status(500).json({ error: 'Failed to fetch AI providers' });
  }
});

/**
 * POST /api/ai/generate
 * Generate a single AI image
 */
router.post('/generate', authenticateToken, requireAuthor, async (req, res) => {
  try {
    const { prompt, provider, size, style, quality } = req.body;

    if (!prompt) {
      return res.status(400).json({ error: 'Prompt is required' });
    }

    const options = {
      provider: provider || 'openai',
      size: size || '1024x1024',
      style: style || 'vivid',
      quality: quality || 'standard'
    };

    const images = await aiService.generateAndSave(prompt, options);

    // Log generation
    await db.query(`
      INSERT INTO ai_generation_history (prompt, provider, options, result_count, generated_by)
      VALUES ($1, $2, $3, $4, $5)
    `, [prompt, options.provider, JSON.stringify(options), images.length, req.user.id]);

    logger.info(`AI image generated by ${req.user.username}: ${prompt.substring(0, 50)}...`);

    res.json({
      success: true,
      images,
      prompt,
      provider: options.provider
    });
  } catch (error) {
    logger.error('Error generating AI image:', error);
    res.status(500).json({ error: error.message || 'Failed to generate image' });
  }
});

